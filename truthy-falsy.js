// Truthy and Falsy values

//  falsy values
// 1: false
// 2: 0
// 3: ""
// 4: undefined
// 5: null
// 6: NaN

//  truthy values
// 1: "false"
// 2: " "
// 3: "0"
// 4: []

// example 1
const number = 0;
if(number){
    console.log('condition is true');
}
else{
    console.log('condition is false');
}


